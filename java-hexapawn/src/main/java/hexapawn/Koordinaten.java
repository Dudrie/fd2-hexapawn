package hexapawn;

/**
 * Koordinaten
 */
public class Koordinaten {

    private final int x;
    private final int y;

    public Koordinaten(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}